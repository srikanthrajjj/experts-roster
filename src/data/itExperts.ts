export const MOCK_IT_EXPERTS: ITExpert[] = RAW_MOCK_IT_EXPERTS.map(expert => {
  const firstAvail = expert.allocations.find(a => a.percentage < 100);
  
  const updatedCommitments = expert.upcomingCommitments.map((commitment, index) => {
    const block = expert.allocations[index % expert.allocations.length];
    if (!block) return commitment;
    
    const start = new Date(block.weekStart);
    const end = new Date(start);
    end.setDate(end.getDate() + 6);
    const fmt = (d: Date) => d.toLocaleDateString('en-GB', { day: 'numeric', month: 'short' });
    const formattedRange = `${fmt(start)}–${fmt(end)}`;
    
    return {
      ...commitment,
      date: formattedRange,
    };
  });

  return {
    ...expert,
    nextAvailableDate: firstAvail ? firstAvail.weekStart : expert.nextAvailableDate,
    upcomingCommitments: updatedCommitments,
  };
});

export function getExpertById(id: string): ITExpert | undefined {
  const saved = localStorage.getItem('expert_dashboard_data');
  const list = saved ? JSON.parse(saved) : MOCK_IT_EXPERTS;
  return list.find((e: any) => e.id === id);
}

export function computeKPIs(experts: ITExpert[]) {
  const available = experts.filter((e) => e.availabilityStatus === 'available').length;
  const partiallyAvailable = experts.filter((e) => e.availabilityStatus === 'partially_allocated').length;
  const fullyBooked = experts.filter((e) => e.availabilityStatus === 'fully_booked').length;
  const onLeave = experts.filter((e) => e.availabilityStatus === 'on_leave').length;
  const availableNext30 = experts.filter((e) => e.availabilityStatus === 'available' || e.availabilityStatus === 'partially_allocated').length;
  const overallocated = experts.filter((e) => e.allocationPercent > 80).length;
  const underutilized = experts.filter((e) => e.utilizationPercent < 30 && e.availabilityStatus !== 'on_leave').length;

  return {
    total: experts.length,
    availableThisWeek: available,
    availableNext30,
    partiallyAvailable,
    fullyBooked,
    onLeave,
    overallocated,
    underutilized,
    benchAvailable: experts.filter((e) => e.benchAvailable).length,
  };
}

export function formatWeekLabel(weekStart: string): string {
  const start = new Date(weekStart);
  const end = new Date(start);
  end.setDate(end.getDate() + 6);
  const fmt = (d: Date) => d.toLocaleDateString('en-GB', { day: 'numeric', month: 'short' });
  return `${fmt(start)}–${fmt(end)}`;
}

export function getGanttWeeks(experts: ITExpert[]): string[] {
  if (experts.length === 0) return [];
  return experts[0].allocations.map((a) => a.weekStart);
}
